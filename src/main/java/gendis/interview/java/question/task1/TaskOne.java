package gendis.interview.java.question.task1;

/**
 * A class provides a single method which accepts an integer and convert it in String format.
 *
 * @author skl
 * @since 2022-01-26
 */
public class TaskOne {

    /**
     * The method accepts an integer number and convert to String format.
     * e.g. input : 14     output : "14"
     *
     * @param number a value of integer
     * @return Numerical value in String.
     */
    public String getIntAsString(int number) {
        return String.valueOf(number);
    }
}
